    brr = [0]
    cs = 0
    for a in arr:
        cs += a
        brr.append(cs)

    c = Counter([])

    for i,b in enumerate(brr):
        c[b-i] += 1

    # console(c)

    return sum([v*(v-1)//2 for k,v in c.items()])


